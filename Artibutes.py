import numpy as np 
a=np.array([[10,20,30],[40,50,60]])
print(np.shape(a)) #gives us shape of the array 
print(np.size(a)) #give us size of the array 
print(np.ndim(a)) #give us dimension of the array 
print(a.dtype) #give us dtype
print(len(a)) #gives us length of the array
print(a.astype(float)) #convert into datatypes 
print(a.astype(str))